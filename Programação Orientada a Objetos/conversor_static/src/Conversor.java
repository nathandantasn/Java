
public class Conversor {

	public static double metersToMillimeters(double valor) {
		return valor * 1000;
	}
	
	public static double metersToCentimeters(double valor) {
		return valor * 100;
	}
	
	public static double metersToDecimeters(double valor) {
		return valor * 10;
	}
	
	public static double millimetersToMeters(double valor) {
		return valor / 1000;
	}
	
	public static double centimetersToMeters(double valor) {
		return valor / 100;
	}
	
	public static double decimetersToMeters(double valor) {
		return valor / 10;
	}
	
}
