
public class aluno {

	public aluno () {}
		
		public double soma(double x, double y) {
			return x + y;
		}

		public double sub(double x, double y) {
			return x - y;
		}
		
		public double mult(double x, double y) {
			return x * y;
		}
		
		public double div(double x, double y) {
			return x / y;
		}
}
